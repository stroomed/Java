    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed

        String usuario = "user";
        String contrasenia = "admin";
        
        int intentos = 0;
        
        String user = txtUser.getText();
        String pass = new String (txtPass.getPassword());
        
        if (user.equals(usuario) && pass.equals(contrasenia)){
            menuAdmin openMenu = new menuAdmin();
            openMenu.setVisible(true);
            dispose();
        } else {
            intentos = intentos + 1;
            if (intentos == 3){
                JOptionPane.showMessageDialog (this, "Límite de intentos alcanzado, intente más tarde.");
                System.exit(0);
            }else {
                JOptionPane.showMessageDialog (this, "Usuario y/o contraseña incorrecto(s). Por favor, intente nuevamente.");
            }
            
            
            
        }
        
        
        
    }//GEN-LAST:event_loginButtonActionPerformed

