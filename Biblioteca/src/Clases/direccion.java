
package Clases;

public class direccion {
    
    private String rut, calle;

    public direccion(String rut, String calle) {
        this.rut = rut;
        this.calle = calle;
    }
    
    public String getRut() {
        return rut;
    }

    public void setRut(String rut) {
        this.rut = rut;
    }

    public String getCalle() {
        return calle;
    }

    public void setCalle(String calle) {
        this.calle = calle;
    }
            
    
    
    
    
    
}
